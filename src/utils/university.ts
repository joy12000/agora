/**
 * 대학교 도메인을 사람이 읽기 좋은 한글 명칭으로 변환합니다.
 * 등록되지 않은 도메인의 경우 접두사 포맷팅(예: gachon.ac.kr -> Gachon대)을 수행합니다.
 */
export const resolveUniversityName = (domainOrName: string | null | undefined): string => {
  if (!domainOrName) return '대학생';
  
  // 이미 한글 대학교 명칭인 경우 바로 반환
  if (domainOrName.includes('대학교') || domainOrName.includes('학교') || domainOrName === 'KAIST' || domainOrName === 'UNIST' || domainOrName === 'GIST' || domainOrName === 'DGIST') {
    return domainOrName;
  }

  const cleanDomain = domainOrName.toLowerCase().trim();
  
  // 매핑 테이블에서 검출
  if (KR_UNIVERSITY_MAP[cleanDomain]) {
    return KR_UNIVERSITY_MAP[cleanDomain];
  }

  // 예외 처리: .ac.kr / .edu 처리
  if (cleanDomain.endsWith('.ac.kr')) {
    const name = cleanDomain.replace('.ac.kr', '');
    return name.charAt(0).toUpperCase() + name.slice(1) + '대';
  }
  if (cleanDomain.endsWith('.edu')) {
    const name = cleanDomain.replace('.edu', '');
    return name.charAt(0).toUpperCase() + name.slice(1) + '대';
  }

  return domainOrName;
};
